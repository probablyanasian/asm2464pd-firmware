static void phy_set_e0_07_pair(uint16_t reg) { RMW(reg, 0x0F, 0xE0); RMW(reg + 1, 0x0F, 0x70); }  /* c335 */
static void phy_clr_lo4(uint16_t reg) { PR(reg) &= 0xFE; PR(reg) &= 0xFD; PR(reg) &= 0xFB; PR(reg) &= 0xF7; }  /* c30e */
