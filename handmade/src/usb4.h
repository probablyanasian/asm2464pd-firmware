static void pcie_tunnel_adapter_enable(void) {  /* b401 */
  REG_CPU_MODE_NEXT &= 0xEF;
  pcie_tunnel_adapter_config();
  P1_WR(P1_TUNNEL_CFG_4084, 0x22);
  P1_WR(P1_TUNNEL_CFG_5084, 0x22);
  REG_PCIE_TUNNEL_CTRL |= 0x01;
  REG_TUNNEL_ADAPTER_MODE |= 0x01;
  REG_TUNNEL_ADAPTER_MODE = (uint8_t)((REG_TUNNEL_ADAPTER_MODE & 0x0F) | 0xF0);
  REG_PCIE_TUNNEL_CTRL &= 0xFE;
  REG_PCIE_PERST_CTRL = (uint8_t)((REG_PCIE_PERST_CTRL & 0xFE) | 0x01);
  REG_TUNNEL_LINK_STATE &= 0xFE;
  REG_PCIE_TUNNEL_CFG = (uint8_t)((REG_PCIE_TUNNEL_CFG & 0xEF) | 0x10);
  P1_WR(P1_TUNNEL_CFG_6043, 0x70);
  P1_WR(P1_TUNNEL_CFG_6025, (uint8_t)((P1_RD(P1_TUNNEL_CFG_6025) & 0x7F) | 0x80));
}

static void u4c_phy_cdr_seed(uint8_t mode) {  /* e0d9 */
  if (mode == 4) {
    REG_PHY_RXPLL_RESET = 0x3E; REG_PHY_CTRL_C20F = 0x08; REG_PHY_CDR_SEED_C210 = 0x08; REG_PHY_CDR_SEED_C211 = 0x2E; REG_PHY_CDR_SEED_C212 = 0x3E;
    REG_PHY_CDR_SEED_C214 = 0x00; REG_PHY_CDR_SEED_C215 = 0x20; REG_PHY_CDR_SEED_C216 = 0x00; REG_PHY_CDR_SEED_C217 = 0x3F;
  }
}

static void u4c_lane_timer_gate(uint8_t mode) {  /* e7c1 */
  if (mode == 1) { REG_TIMER_ENABLE_B &= 0xFD; REG_TIMER_ENABLE_A &= 0xFD; }
  else if (u4_connect_gate & 0x10) { REG_TIMER_ENABLE_B = (REG_TIMER_ENABLE_B & 0xFD) | 0x02; REG_TIMER_ENABLE_A = (REG_TIMER_ENABLE_A & 0xFD) | 0x02; }
}

static void usb4_connect_u4(void) {
  u4_connect_gate |= 0x01;
  if (u4_connect_gate & 0x01) {
    REG_LINK_STATUS_E716 = (REG_LINK_STATUS_E716 & 0xFC) | 0x03;
    REG_CPU_CTRL_CA81 &= 0xFE;
    REG_CPU_MODE_NEXT = (REG_CPU_MODE_NEXT & 0x1F) | 0x60;
  }
  boot_phy_set_link_mode(0);
  u4c_lane_timer_gate(1);
  u4c_phy_cdr_seed(0);
  if (u4_pd.enter_usb_accepted == 0) {
    if (u4_pd.connect_route_latch == 0) return;
    u4_cfg.route_mode = 0x81;
    u4_cfg.lane_gate_sel = 0x02;
  } else {
    u4_cfg.route_mode = (u4_cfg.mode_flag & MODE_FLAG_ROUTE_MASK);
    if (u4_cfg.dp_alt_mode == 0x03) {
      if (u4_pd.usb3_fallback_flag == 0) { u4_cfg.route_mode = 2; u4_cfg.lane_gate_sel = 1; }
      else                 { u4_cfg.route_mode = 1; u4_cfg.lane_gate_sel = 2; }
    }
    if (u4_cfg.route_mode & 0x02) {
      REG_LINK_STATUS_E716 &= 0xFC;
      REG_LINK_STATUS_E716 = (REG_LINK_STATUS_E716 & 0xFC) | 0x03;
      SB_WR(SB_ROUTE_ACK, 0x02);
    }
  }
  sb_assert();
}

static void sb_router_event_handler(void);

static uint8_t u4rop_underflow(void) {
  int8_t i;
  for (i = 3; i >= 0; i--)
    if (u4_rop_limit[i] != u4_rop_cfg_addr[i]) return u4_rop_limit[i] < u4_rop_cfg_addr[i];
  return 0;
}

static void u4rop_read_flash_resp(void) {
  uint8_t xfer_len, shadow_ptr0, shadow_ptr1, shadow_ptr2;
  uint16_t w16, g;
  if (u4rop_underflow()) xfer_len = 0x80;
  else                   xfer_len = (uint8_t)(u4_rop_limit[3] - u4_rop_cfg_addr[3]);
  REG_I2C_DMA_ENABLE = (REG_I2C_DMA_ENABLE & 0xF9) | 0x02;

  shadow_ptr2 = u4_rop_limit[2];
  w16 = (uint16_t)((((uint16_t)u4_rop_limit[1] << 8) | u4_rop_limit[0]) + 3);
  shadow_ptr0 = (uint8_t)(w16 & 0xFF);
  shadow_ptr1 = (uint8_t)(w16 >> 8);

  REG_FLASH_MODE = REG_FLASH_MODE & 0xFE;
  REG_FLASH_BUF_OFFSET_LO = 0;
  REG_FLASH_BUF_OFFSET_HI = (REG_FLASH_BUF_OFFSET_HI & 0xFC);
  REG_FLASH_CMD = 0x03;
  REG_FLASH_ADDR_LO = shadow_ptr0;
  REG_FLASH_ADDR_MD = shadow_ptr1;
  REG_FLASH_ADDR_HI = shadow_ptr2;
  REG_FLASH_DATA_PAGE_CNT = 0;
  REG_FLASH_DATA_BYTE_OFS = 0;
  REG_FLASH_CSR = 0x01;
  for (g = 0; (REG_FLASH_CSR & 0x01) && g < 0x4000; g++) { }
  REG_FLASH_MODE = REG_FLASH_MODE & 0xEF;
  REG_FLASH_MODE = REG_FLASH_MODE & 0xDF;
  REG_FLASH_MODE = REG_FLASH_MODE & 0xBF;
  REG_FLASH_MODE = REG_FLASH_MODE & 0x7F;

  w16 = (uint16_t)((((uint16_t)u4_rop_limit[1] << 8) | u4_rop_limit[0]) + xfer_len);
  u4_rop_limit[0] = (uint8_t)(w16 & 0xFF);
  u4_rop_limit[1] = (uint8_t)(w16 >> 8);

  REG_DMA_MODE      = 0x70;
  XDATA_REG8(0xC8B1) = 0x00;
  REG_DMA_CHAN_AUX   = 0xEA;
  REG_DMA_CHAN_AUX1  = 0x00;
  REG_DMA_XFER_CNT_HI = (uint8_t)(xfer_len ? 0xFE : 0xFF);
  REG_DMA_XFER_CNT_LO = (uint8_t)(xfer_len - 1);
  REG_DMA_CHAN_CTRL2 = 0x10;
  REG_DMA_TRIGGER = 0x01;
  for (g = 0; (REG_DMA_TRIGGER & 0x01) && g < 0x4000; g++) { }
}

static void cm_routerop_mailbox(void) {
  uint8_t op;
  if (REG_SYS_CTRL_EA90 != 0x5A) return;

  if (u4_routerop_mbox_state == RMBOX_IDLE) {
    op = REG_ROUTEROP_OPCODE_EA80;
    u4_routerop_mbox_opcode = op;
    if (op == 0xE2) {
      if (REG_ROUTEROP_CFG_EA81 == 0x50 || REG_ROUTEROP_CFG_EA81 == 0x51) {
        uint8_t i;
        for (i = 0; i < 4; i++) u4_rop_cfg_addr[i] = XDATA_REG8V(0xEA82 + i);
        for (i = 0; i < 4; i++) u4_rop_limit[i] = u4_rop_cfg_addr[3];
        u4rop_read_flash_resp();
        if (!u4rop_underflow() &&
            (u4_rop_limit[0] != u4_rop_cfg_addr[0] || u4_rop_limit[1] != u4_rop_cfg_addr[1] ||
             u4_rop_limit[2] != u4_rop_cfg_addr[2] || u4_rop_limit[3] != u4_rop_cfg_addr[3]))
          u4_routerop_mbox_state = RMBOX_MULTIPKT_1;
      }
    }
    REG_SYS_CTRL_EA90 = 0xA5;
    return;
  }

  if (u4_routerop_mbox_state == RMBOX_MULTIPKT_1) {
    if (u4_routerop_mbox_opcode == 0xE2) {
      u4rop_read_flash_resp();
      if (u4rop_underflow()) u4_routerop_mbox_state = RMBOX_IDLE;
      REG_SYS_CTRL_EA90 = 0xA5;
      return;
    }
    u4_routerop_mbox_state = RMBOX_IDLE;
  } else if (u4_routerop_mbox_state == RMBOX_MULTIPKT_2) {
    if (u4_routerop_mbox_opcode == 0xE3) {
      u4_routerop_mbox_state = RMBOX_IDLE;
      REG_SYS_CTRL_EA90 = 0xA5;
      return;
    }
    u4_routerop_mbox_state = RMBOX_IDLE;
  }
}

