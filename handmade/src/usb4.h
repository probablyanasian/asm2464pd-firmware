static void usb4_sec_adapter_link_event(void) {  /* c105 */
  uint8_t p1407 = P1_RD(P1_USB4_ADP_EVENT_STATUS_1407);
  uint8_t p1508 = P1_RD(P1_USB4_TUNNEL_EVENT_STATUS_1508);
  uint8_t p1603 = P1_RD(P1_USB4_BOOT_TAIL_EVENT_1603);

  if (p1407 & 0x01) {
    if (P1_RD(P1_USB4_WIDTH_EVENT_1203) & 0x80) {
      P1_WR(P1_USB4_WIDTH_EVENT_1203, 0x80);
      if ((u4_connect_gate & 0x10) && (u4_cfg.route_mode & 0x81)) u4_cfg.route_mode |= 0x04;
    }
    if (P1_RD(P1_USB4_WIDTH_EVT_124E) & 0x02) {
      P1_WR(P1_USB4_WIDTH_EVT_124E, 0x02);
      u4lb_reg_set_bit7(0x35);
      ENG_DESC_WR_CLR(0x36, 0x03);
      u4lb_desc_commit_noset(0x03);
      if (P1_RD(P1_DESC_RESULT_1243) & 0x80) u4lb_link_phy_reconfig();
    }
  }

  if (p1407 & 0x08) {
    u4lb_tunnel_event_dispatch(0);
  }

  if (p1603 & 0x01) {
    P1_WR(P1_USB4_BOOT_TAIL_EVENT_1603, 0x01);
    if (u4_cfg.route_mode & 0x02) {
      if (REG_POWER_STATUS & 0x40) u4_entered_usb_mode = 1;
      u4lb_cpu_ext_kick();
      u4_pd.cm_dispatch_sel = 0x69;
    }
  } else if (p1603 & 0x02) {
    P1_WR(P1_USB4_BOOT_TAIL_EVENT_1603, 0x02);
  }
}

static void usb4_int_demux(void) {
  uint8_t int_sources = REG_INT_PCIE_NVME;
  if (int_sources & 0x20) {
    sb_router_event_handler();
  }
  if (int_sources & 0x10) {
    usb4_sec_adapter_link_event();
  }
  if (REG_NVME_EVENT_STATUS & 0x01) {
    REG_NVME_EVENT_ACK = 1;
    cm_routerop_mailbox();
  }
  if (int_sources & 0x0F) {
    { uint8_t tunnel_status = REG_PHY_RXPLL_TRIGGER;
      if (tunnel_status & 0x04) REG_PHY_RXPLL_TRIGGER = 0x04;
      if (tunnel_status & 0x08) REG_PHY_RXPLL_TRIGGER = 0x08; }
  }
}

